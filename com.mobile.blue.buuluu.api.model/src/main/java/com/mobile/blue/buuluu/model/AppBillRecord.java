package com.mobile.blue.buuluu.model;

/**
 * 
 * @author More
 *账单记录  2015-7-28
 */
public class AppBillRecord extends AbstractEntity {

	/**
	 * 
	 */
	private static final long serialVersionUID = 361918166037702003L;

	private String userId;//账单记录的用户id
	
	private Integer  type;//账单类型，1是收入，2是支出
	
	private Integer status;//账单状态，0是未完成，1是完成
	
	private Float makeFlows;//账单消费的流量币
	
	private String description;//账单的描述

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Float getMakeFlows() {
		return makeFlows;
	}

	public void setMakeFlows(Float makeFlows) {
		this.makeFlows = makeFlows;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}
