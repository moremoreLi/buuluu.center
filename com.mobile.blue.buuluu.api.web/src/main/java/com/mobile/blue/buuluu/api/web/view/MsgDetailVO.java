package com.mobile.blue.buuluu.api.web.view;

import java.io.Serializable;

/**
 * 
 * @author More
 *2015-8-4
 * 消息返回详情对象
 */
public class MsgDetailVO implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 370984734871028865L;
	
	private String info;//消息内容
	private long createTime;//消息最新时间
	private boolean readFlag;//是否处理状态，false未处理，true已处理
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public long getCreateTime() {
		return createTime;
	}
	public void setCreateTime(long createTime) {
		this.createTime = createTime;
	}
	public boolean isReadFlag() {
		return readFlag;
	}
	public void setReadFlag(boolean readFlag) {
		this.readFlag = readFlag;
	}
}
