public class InviteFriendVO implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -7320184720957571669L;
	

	private float freeFlows;//赠送的流量
	private String detailUrl;//详情的url
	private String actCode;//校验码
	private String msg;//发送短信邮件的主要内容
	private String reigisterUrl;//注册地址
	public float getFreeFlows() {
		return freeFlows;
	}
	public void setFreeFlows(float freeFlows) {
		this.freeFlows = freeFlows;
	}
	public String getDetailUrl() {
		return detailUrl;
	}
	public void setDetailUrl(String detailUrl) {
		this.detailUrl = detailUrl;
	}
	public String getActCode() {
		return actCode;
	}
	public void setActCode(String actCode) {
		this.actCode = actCode;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getReigisterUrl() {
		return reigisterUrl;
	}
	public void setReigisterUrl(String reigisterUrl) {
		this.reigisterUrl = reigisterUrl;
	}
	
}
